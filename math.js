/**
 *
 * @param {number} num1
 * @param {number} num2
 * @returns
 */
function add(num1, num2) {
  return num1 + num2;
}
/**
 *
 * @param {number} num1
 * @param {number} num2
 * @returns
 
 */
function subtract(num1, num2) {
  return num1 - num2;
}

module.exports.add = add;
module.exports.subtract = subtract;
